def escape_sql(text):
    """–≠–∫—Ä–∞–Ω–∏—Ä—É–µ—Ç –æ–¥–∏–Ω–∞—Ä–Ω—ã–µ –∫–∞–≤—ã—á–∫–∏ –¥–ª—è SQL"""
